<template>
  <main class="page">
    <section class="hero">
      <p class="eyebrow">Vue + Axum + Utoipa</p>
      <h1>Full-stack starter</h1>
      <p>
        Backend runs on Axum with autogenerated OpenAPI docs served from <code>/docs</code>.
        The frontend is powered by Vite + Vue and proxies API calls during local development.
      </p>
      <div class="greeting" v-if="!loading && greeting">
        <span class="label">Latest message</span>
        <strong>{{ greeting }}</strong>
      </div>
      <p v-if="loading">Fetching greetingâ€¦</p>
      <p v-if="error" class="error">{{ error }}</p>
    </section>

    <GreetingCard />
  </main>
</template>

<script setup>
import { onMounted, ref } from 'vue';
import GreetingCard from './components/GreetingCard.vue';
import { apiFetch } from './lib/apiClient.js';

const greeting = ref('');
const loading = ref(true);
const error = ref('');

const fetchGreeting = async () => {
  loading.value = true;
  error.value = '';

  try {
    const response = await apiFetch('/greeting');
    if (!response.ok) {
      throw new Error('Unable to contact backend');
    }

    const payload = await response.json();
    greeting.value = payload.message;
  } catch (err) {
    error.value = err instanceof Error ? err.message : 'Unknown error';
  } finally {
    loading.value = false;
  }
};

onMounted(fetchGreeting);
</script>

